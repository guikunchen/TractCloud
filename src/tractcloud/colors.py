def get_tract_color(index):
    """Return (R, G, B) color for a tract by index (1-based).

    Returns colors matching Slicer's GenericColors table.
    Wraps around if index exceeds table size.
    """
    idx = index % len(GENERIC_COLORS)
    if idx == 0:
        idx = 1  # skip background black
    return GENERIC_COLORS[idx]
